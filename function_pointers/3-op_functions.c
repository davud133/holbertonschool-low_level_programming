#include "3-calc.h"

/* Add two numbers */
int op_add(int a, int b)
{
	return (a + b);
}

/* Subtract b from a */
int op_sub(int a, int b)
{
	return (a - b);
}

/* Multiply two numbers */
int op_mul(int a, int b)
{
	return (a * b);
}

/* Divide a by b */
int op_div(int a, int b)
{
	return (a / b);
}

/* Modulo of a by b */
int op_mod(int a, int b)
{
	return (a % b);
}

